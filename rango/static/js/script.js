document.addEventListener('DOMContentLoaded', function() {
    // Contact Us Message Handler
    const contactBtn = document.getElementById('contact-submit-btn');
    if (contactBtn) {
        contactBtn.addEventListener('click', function(e) {
            e.preventDefault();
            alert('Message Sent!');
            window.location.href = this.getAttribute('data-url');
        });
    }

    // AJAX Remove Log Handler
    const removeLogBtns = document.querySelectorAll('.btn-remove-log');
    removeLogBtns.forEach(btn => {
        btn.addEventListener('click', function(e) {
            e.preventDefault();
            const logId = this.getAttribute('data-log-id');
            const url = this.getAttribute('data-url');
            
            // Extract the CSRF Token generated by Django on the page
            const csrfTokenInput = document.querySelector('[name=csrfmiddlewaretoken]');
            if (!csrfTokenInput) return;
            
            const csrfToken = csrfTokenInput.value;

            // Execute asynchronous POST request
            fetch(url, {
                method: 'POST',
                headers: {
                    'X-Requested-With': 'XMLHttpRequest',
                    'X-CSRFToken': csrfToken,
                    'Content-Type': 'application/json'
                }
            })
            .then(response => response.json())
            .then(data => {
                if (data.status === 'success') {
                    // Smoothly fade out the table row and remove from DOM
                    const row = document.getElementById('log-row-' + logId);
                    if (row) {
                        row.style.transition = 'opacity 0.3s ease';
                        row.style.opacity = '0';
                        setTimeout(() => row.remove(), 300);
                    }
                } else {
                    alert('Error deleting log: ' + data.message);
                }
            })
            .catch(error => {
                console.error('AJAX Error:', error);
                alert('An error occurred while deleting the log.');
            });
        });
    });
});
